import { describe, expect, it } from 'vitest'
import { getZodiacSign, isValidIsoDate } from '../src/domain/zodiac'

describe('getZodiacSign', () => {
  it.each([
    ['2024-01-19', 'Козерог'], ['2024-01-20', 'Водолей'],
    ['2024-02-18', 'Водолей'], ['2024-02-19', 'Рыбы'],
    ['2024-03-20', 'Рыбы'], ['2024-03-21', 'Овен'],
    ['2024-04-19', 'Овен'], ['2024-04-20', 'Телец'],
    ['2024-05-20', 'Телец'], ['2024-05-21', 'Близнецы'],
    ['2024-06-20', 'Близнецы'], ['2024-06-21', 'Рак'],
    ['2024-07-22', 'Рак'], ['2024-07-23', 'Лев'],
    ['2024-08-22', 'Лев'], ['2024-08-23', 'Дева'],
    ['2024-09-22', 'Дева'], ['2024-09-23', 'Весы'],
    ['2024-10-22', 'Весы'], ['2024-10-23', 'Скорпион'],
    ['2024-11-21', 'Скорпион'], ['2024-11-22', 'Стрелец'],
    ['2024-12-21', 'Стрелец'], ['2024-12-22', 'Козерог'],
  ])('возвращает %s для %s', (date, sign) => expect(getZodiacSign(date)).toBe(sign))

  it('отклоняет несуществующие даты', () => {
    expect(isValidIsoDate('2025-02-29')).toBe(false)
    expect(isValidIsoDate('2024-02-29')).toBe(true)
    expect(getZodiacSign('2024-13-01')).toBeUndefined()
  })
})
